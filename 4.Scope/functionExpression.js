function fun() {
  // This is function definition
  console.log("Hello World");
}

let gun = function () {
  // This is example of Function Expression
  console.log("Hello ji ");
};
gun();
fun();

(function () {
  console.log("Main wapas aaunga");
}); // This is also an example of Function Expression

